package String;

public class CharAt {
	    public static void main(String[] args) {
	        String str1 = "AlRightTechnology";
	        String str2 = "SeleniumAutomationtool";
	        String str3 = "shahul";
	        String str4 = "j a v a p r o g r a m";
	        String str5 = "9095484678";
	        System.out.println("str1:"+str1.charAt(5));
	        System.out.println("str2:"+str2.charAt(11));
	        System.out.println("str3:"+str3.charAt(4));
	        System.out.println("str4:"+str4.charAt(8));
	        System.out.println("str5:"+str5.charAt(8));
	        
}
}
