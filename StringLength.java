package String;

public class StringLength {
	    public static void main(String[] args) {
	        String str1 = "AlRightTechnology";
	        String str2 = "SeleniumAutomationtool";
	        String str3 = "shahul";
	        String str4 = "j a v a p r o g r a m";
	        String str5 = "9095484678";
	        System.out.println("Length of String 1: " + str1.length());
	        System.out.println("Length of String 2: " + str2.length());
	        System.out.println("Length of String 3: " + str3.length());
	        System.out.println("Length of String 4: " + str4.length());
	        System.out.println("Length of String 5: " + str5.length());
	    }
	}

